      <div class="section__container container">
        <h2 class="section__title">Trois pilliers</h2>
        <p class="section__text">
          Lhiving offre un accompagnement sur mesure à travers de trois piliers : l’antenne, l’accompagnement ambulatoire et l’accompagnement semi-résidentiel avec 18 logements de transit.
        </p>
        <div class="pilliers">
          <div class="pillier__item col-12 col-md-4">
            <img src="assets/img/Antenne-pillier.jpg" alt="" class="pilliers__img">
            <p class="pilliers__text">
              La fonction de <span class="pilliers__highlight">l’antenne</span> est triple: tout d’abord c’est l’endroit où les nouvelles demandes sont enregistrées et un premier rendez-vous est fixé avec l’un des travailleurs. En outre, c’est un point de contact qui peut donner des réponses à des demandes ponctuelles très concrètes. Et finalement, c’est la porte d’entrée vers les autres piliers de Lhiving. Lorsque la demande d’aide est trop complexe ou trop grand pour pouvoir y répondre au niveau de l’antenne, on peut offrir à la personne une guidance plus intensive avec un accompagnateur fixe, que ça soit ambulatoire ou semi-résidentiel.
            </p>
          </div>
          <div class="pillier__item col-12 col-md-4">
            <img src="http://placekitten.com/335/225" alt="" class="pilliers__img">
            <p class="pilliers__text">
              Un <span class="pilliers__highlight">accompagnement ambulatoire</span> comprend un soutien au niveau psychosocial et administratif. La guidance est développée en partenariat avec et adapté au client et est de nature intégrale. Vivre avec une maladie chronique grave affecte de nombreux aspects de la vie quotidienne. Lhiving accompagne la personne vers d’autres services et fait des visites à domicile.
            </p>
          </div>
          <div class="pillier__item col-12 col-md-4">
            <img src="http://placekitten.com/335/225" alt="" class="pilliers__img">
            <p class="pilliers__text">
            <span class="pilliers__highlight">L’accompagnement en semi-résidentiel</span> est destiné aux personnes ayant un grand besoin d’être accompagné et pour qui un séjour temporaire dans un de nos 18 logements de transit est nécessaire et possible. Cela est toujours lié à une guidance intensive et intégrale, le but étant également de travailler vers une solution de logement permanente pour la personne, de préférence dans un logement social.
            </p>
          </div>
        </div>
      </div>
